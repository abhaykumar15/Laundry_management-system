
package model;

public class Hostel {
    private String name;
    private String block;
    private String laundryBlock;

    // getters and setters
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }

    public String getBlock() { return block; }
    public void setBlock(String block) { this.block = block; }

    public String getLaundryBlock() { return laundryBlock; }
    public void setLaundryBlock(String laundryBlock) { this.laundryBlock = laundryBlock; }
}